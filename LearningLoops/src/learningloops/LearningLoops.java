
package learningloops;


public class LearningLoops {

    
    public static void main(String[] args) {
        
        
        
        System.out.println();
        
        
        for(int t=1; t<=10; t++)
          for(int x=1; x<10; x++)
          System.out.print(x);
        
        
        System.out.println();
        
        
        
        
    }
    
}
